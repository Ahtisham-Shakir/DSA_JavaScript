// Traversing Means visiting every element in the array

let data = [1, 23, 43, 20, 45, 21, 50];
for (let i = 0; i < data.length; i++) {
  console.log(`Array ${i} is ${data[i]}`);
}

// Accessing means accessing individual element from the array
let index = 5;
console.log(`element at ${index}th index is ${data[index]}`);
